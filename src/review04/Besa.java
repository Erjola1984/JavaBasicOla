package com.neotech.review04;

public class Besa {
	
	//A METHOD WITH NO PARAMETERS
	void addTwoNumbers() {
		int result = 2 + 5;
	}
	//a method with 2 parameters but not return type
	void addTwoNumbersThatIGive(int a, int b) {
		int result = a + b;
		
	}
	
	//a method with no parameters and with return type
	int addTwoNumbersAndGiveMeTheResult() {
		int result = 2 + 5;
		return result;
		
	}
	//a method with 2 parameters and with return type
	int addTwoNumbersThatIGiveAndGiveMeTheResult (int num1, int num2){
		int result = num1 + num2;
		return result;
	}

		
}
